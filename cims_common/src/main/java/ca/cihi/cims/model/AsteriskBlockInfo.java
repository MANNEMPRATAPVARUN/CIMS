package ca.cihi.cims.model;

public class AsteriskBlockInfo {

	private String code;

	private String userDescEng;

	private String userDescFra;

	public String getCode() {
		return code;
	}

	public String getUserDescEng() {
		return userDescEng;
	}

	public String getUserDescFra() {
		return userDescFra;
	}

	public void setCode(final String code) {
		this.code = code;
	}

	public void setUserDescEng(final String userDescEng) {
		this.userDescEng = userDescEng;
	}

	public void setUserDescFra(final String userDescFra) {
		this.userDescFra = userDescFra;
	}
}
